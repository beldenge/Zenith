package com.ciphertool.zenith.inference.evaluator.model;

import org.junit.jupiter.api.Test;

import java.util.Arrays;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertNotNull;

public class RestServiceEvaluationRequestTest {
    @Test
    public void given_validInput_when_settingSequences_then_returnsExpected() {
        RestServiceEvaluationRequest request = new RestServiceEvaluationRequest();
        request.setSequences(Arrays.asList("alpha", "beta"));

        assertEquals(Arrays.asList("alpha", "beta"), request.getSequences());
        assertNotNull(request.toString());
    }
}